#chomp and chop method
puts "hello\n".chomp
puts "hello".chop

#length and count method
puts "hello".length
puts "hello".count("l")

#concat method
puts "hello".concat(" world")

#delete method
puts "hello".delete("l")

#delete_suffix and delete_prefix
puts "hello world hello".delete_suffix("hello")
puts "hello world hello".delete_prefix("hello")

#capitalize method
puts "hello world".capitalize

#strip method
puts " hello ".strip

#reverse method
puts "hello".reverse

#upcase and downcase
puts "Hello".upcase
puts "Hello".downcase

#swapcase method
puts "Hello".swapcase